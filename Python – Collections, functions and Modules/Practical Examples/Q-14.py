# Write a Python program to access values using keys from a dictionary.

my_dict = {
    'name': 'Alice',
    'age': 30,
    'city': 'New York',
    'job': 'Manager',
    'hobby': 'Traveling',
    'country': 'USA'
}

print("Name:",my_dict['name'])
print("Age:",my_dict['age'])
print("City:",my_dict['city'])
print("Job:",my_dict['job'])
print("Hobby:",my_dict['hobby'])
print("Country:",my_dict['country'])