# Write a Python program to add elements to a list using insert() and append().

# Append method to add element in below list
mylist = ["Apple", "Banana", "Cherry", "Orange"]
mylist.append('Grapes')
print(mylist)

# Insert method to add element in below list
my_list = [10, 50, 70, 30]
my_list.insert(2, 60)       # First argument is index no. and second argument is element
print(my_list)